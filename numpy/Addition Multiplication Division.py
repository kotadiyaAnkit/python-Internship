import numpy as np

a = np.array([1, 2, 3])
b = np.array([4, 5, 6])

# Addition
add_result = a + b
print("Addition:", add_result)

# Subtraction
sub_result = a - b
print("Subtraction:", sub_result)

# Multiplication
mul_result = a * b
print("Multiplication:", mul_result)

# Division
div_result = a / b
print("Division:", div_result)
